//a function that returns the maximum of two numbers.
function findMax(number1, number2) {
    return Math.max(number1, number2);
  }
  
  
  const result = findMax(8, 15);
  console.log('Maximum:', result);
  

  //a function that returns the minimum of two numbers.
function findMin(number1, number2) {
    return Math.min(number1, number2);
  }
  
  
  const answer = findMin(8, 15);
  console.log('Minimum:', answer);
  